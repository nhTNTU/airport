		FROM [dbo].[City] c
			INNER JOIN 
				[AirPort] a
		ON 
			c.[city_id] = a.[airP_city_id_FK] AND a.[airP_id] = (SELECT DISTINCT [airP_id] FROM inserted)
	)
	
