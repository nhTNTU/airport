	P.[pass_gender] AS [Стать],
	D.[dT_name] AS [Документ],
	P.[pass_docNumber] AS [Номер документа],
	B.[br_lastName] AS [Прізвище покупця],
	B.[br_firstName] AS [Ім'я покупця],
